package javapractica2;

/**
 *
 * @author ACER01
 */
public class JavaPractica2 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        
        
        /**
         * Enteros
         * 
         * --> byte
         * --> short 
         * --> int
         * --> long
         */
        
        byte enteroB = 12;
        
        short enteroS = 12456;
        
        int enteroI = 24501426;
        
        long enteroL = 1204516327;
        
        System.out.println("Enteros");
        
        System.out.println("Numero entero Byte: " + enteroB);
        System.out.println("Numero entero Short: " + enteroS);
        System.out.println("Numero entero Int: " + enteroI);
        System.out.println("Numero entero Long: " + enteroL);
        
        
        
        /**
         * Decimales
         * 
         * --> Float
         * --> Double
         */
        
        float decimalF = 3.25f;
        
        double decimalD = 1.25698783;
        
        System.out.println(" ");
        System.out.println(" ");
        System.out.println("Decimales");
        
        System.out.println("Numero decimal Float: " + decimalF);
        System.out.println("Numero decimal Double: " + decimalD);
        
        
        
        /**
         * Cadena
         * 
         * --> char 
         * --> String 
         */
        
        char caracterC = 'a';
        String caracterS = "hola mundo";
        
        System.out.println(" ");
        System.out.println(" ");
        System.out.println("Cadenas");
        
        System.out.println("Cadena char: " + caracterC);
        System.out.println("Cadena String: " + caracterS);
        
        
        
        /**
         * Decision
         * 
         * --> boolean
         */
        
        boolean decisionB = true;
        
        System.out.println(" ");
        System.out.println(" ");
        System.out.println("Decisiones");
        
        System.out.println("Desicion boolean: " + decisionB);
        
    }
    
}
